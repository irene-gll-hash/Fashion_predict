def build_fashion_report_prompt(context: str) -> str:
    return f"""
Ты fashion-аналитик.

Подготовь Markdown-отчёт на основе данных ниже.

Задача:
- обобщить найденные категории;
- обобщить найденные стили;
- обобщить найденные материалы и фактуры;
- предложить направления закупки только на основе уже найденных сочетаний категория + стиль + материал.

Жёсткие правила:
- пиши по-русски;
- не упоминай автоматизацию, модели, алгоритмы, классификацию, тестирование, пайплайн, scores, similarity, DINO, SAM, FashionCLIP;
- не пиши про покупателей, спрос, востребованность, продажи, рынок, рост популярности, коммерческий эффект;
- не используй слова: производство, бренды, размеры, расцветки, колодки, натуральные ткани, экокожа, сезонность;
- не придумывай цвета, если их нет в данных;
- не придумывай материалы, если их нет в данных;
- не добавляй товары, которых нет в данных;
- рекомендации формулируй только как "можно рассмотреть";
- в разделе закупки используй только такие форматы:
  "- Можно рассмотреть [категория] в [стиль] с материалом/фактурой [материал]."
  "- Можно рассмотреть [категория] с акцентом на [материал/фактура]."
- если в данных нет цвета, фасона или деталей кроя, не упоминай их.

Структура:

# Fashion Trend AI Report

## 1. Краткое резюме

## 2. Распределение категорий

## 3. Стили и визуальные направления

## 4. Материалы и фактуры

## 5. Направления для закупки

Данные:
{context}
""".strip()