from __future__ import annotations
import json
from typing import Any

def build_gigachat_vision_prompt(context: dict[str, Any]) -> str:
    context_json = json.dumps(context, ensure_ascii=False, indent=2)
    return f"""Ты — fashion-аналитик.

Напиши аналитический отчёт на русском языке по данным Instagram fashion-контента.

Во входных данных уже собраны разрешённые fashion-сигналы. Используй только их.

Не добавляй новые категории, материалы, аксессуары, бренды, стили, аудитории, сезоны или рыночные выводы. Если чего-то нет во входных данных, этого не должно быть в отчёте.

Если данных мало, отчёт должен быть коротким. Не заполняй пустые разделы общими fashion-фразами.

Отчёт должен быть аналитикой, а не советами.

Правила:
- используй только термины из входных данных;
- не расширяй общий термин до подтипов;
- не превращай Shoe в кроссовки, ботинки или сапоги;
- не превращай Bag в рюкзаки, кошельки или клатчи;
- не превращай Textile в хлопок, лён, шёлк, атлас или бархат;
- не делай выводов о спросе, продажах, статусе, аудитории или рынке;
- цвета называй только по переданным hex-кодам и описаниям;
- числа называй вхождениями, а не постами;
- не давай рекомендации, советы или следующие шаги;
- не возвращай JSON.

Структура отчёта:

# Fashion-анализ Instagram-контента

## 1. Краткий аналитический вывод
Коротко опиши главные фактические сигналы из данных.

## 2. Категории товаров
Опиши только категории из входного списка categories.

Для каждой категории укажи:
- название;
- подтверждающие термины;
- количество вхождений;
- силу сигнала;
- короткий аналитический комментарий.

## 3. Цвета и hex-коды
Опиши только цвета из входного списка colors.

Для каждой цветовой группы укажи:
- название группы;
- hex-коды;
- описание оттенков;
- количество вхождений или заметность;
- короткий аналитический комментарий.

Не связывай цвет с товарной категорией, если такая связь не передана во входных данных.

## 4. Материалы
Опиши только материалы из входного списка materials.

Если список пустой или очень короткий, так и напиши. Не добавляй материалы по догадке.

## 5. Аксессуары и детали
Опиши только элементы из входного списка accessories.

Если список пустой или слабый, так и напиши. Не добавляй аксессуары по ассоциации.

## 6. Сила сигналов
Сгруппируй уже перечисленные наблюдения на сильные, средние и слабые.

## 7. Итоговая аналитическая сводка
Кратко собери итог по фактическим сигналам. Не добавляй новых выводов.

Данные:
{context_json}
"""